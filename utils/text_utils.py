import re


def transliterate(text: str) -> str:
    """
    Транслитерирует русский текст в латиницу для безопасных имен файлов.
    
    Алгоритм:
        1. Удаляет метки [litres] и другие метки в квадратных скобках
        2. Удаляет лишние пробелы в начале и конце
        3. Заменяет русские буквы на латинские по таблице транслитерации
        4. Оставляет только буквы, цифры, пробелы, дефисы, подчеркивания и точки
        5. Заменяет все остальные символы на подчеркивания
        6. Заменяет множественные подчеркивания и пробелы на одно подчеркивание
        7. Удаляет подчеркивания в начале и конце
        8. Ограничивает длину до 100 символов
        9. Если результат пустой, возвращает 'book'
    
    Args:
        text: Исходный текст для транслитерации
        
    Returns:
        str: Транслитерированная строка, пригодная для имени файла
    """
    text = re.sub(r'\[litres?\]', '', text, flags=re.IGNORECASE)
    text = re.sub(r'\[.*?\]', '', text)
    text = text.strip()

    translit_map = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
        'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
        'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
        'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
        'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch',
        'Ъ': '', 'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya'
    }

    result = []
    for char in text:
        if char in translit_map:
            result.append(translit_map[char])
        elif char.isalnum() or char in [' ', '-', '_', '.']:
            result.append(char)
        else:
            result.append('_')

    filename = ''.join(result)
    filename = re.sub(r'[_\s]+', '_', filename)
    filename = filename.strip('_')
    if len(filename) > 100:
        filename = filename[:100]

    return filename if filename else 'book'
